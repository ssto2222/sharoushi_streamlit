"""
page_privacy.py - プライバシーポリシー（認証不要）
"""
import streamlit as st
from .constants import COPYRIGHT, APP_NAME, CURRENT_YEAR
from .utils import render_footer
from .auth import is_logged_in


def render():
    if st.button("← 戻る", key="privacy_back"):
        st.session_state.page = "home" if is_logged_in() else "auth"
        st.rerun()

    st.markdown("# プライバシーポリシー")
    st.caption(f"最終更新日：{CURRENT_YEAR}年")
    st.divider()

    st.markdown(f"""
**{APP_NAME}**（以下「本サービス」）は、ユーザーのプライバシーを尊重し、個人情報の保護に取り組みます。

---

### 1. 収集する情報

- **アカウント情報**：メールアドレスおよびパスワード（暗号化して保存）
- **学習データ**：解答履歴、正答率、復習対象の問題などの学習進捗情報
- **利用情報**：サービスの利用状況

---

### 2. 情報の利用目的

- 学習進捗の記録・管理および表示
- アカウントの認証・管理
- サービスの品質向上および不具合対応
- 法令上必要な対応

---

### 3. 情報の第三者提供

ユーザーの同意がある場合または法令に基づく場合を除き、個人情報を第三者に提供しません。

---

### 4. アフィリエイト広告について

本サービスには外部サービスのアフィリエイト広告（おすすめ教材）が表示される場合があります。
広告リンクをクリックして外部サイトにアクセスした場合、その外部サイトのプライバシーポリシーが適用されます。
クリック・購入に関するデータはアフィリエイトプログラムの運営会社により収集される場合があります。

---

### 5. データの保管・セキュリティ

- ユーザーデータは **Supabase**（クラウドデータベース）に保存されます
- パスワードは暗号化して保存され、平文では保管されません

---

### 6. データの削除

アカウント削除はアカウント設定ページから申請できます。
合理的な期間内に対応します。

---

### 7. Cookie・セッション

セッション管理のため認証トークンをブラウザのセッションに一時的に保存します。
ブラウザを閉じることでセッション情報は削除されます。

---

### 8. ポリシーの変更

本ポリシーは予告なく変更される場合があります。
重要な変更がある場合はサービス内でお知らせします。

---

{COPYRIGHT}
    """)

    render_footer()
